'use strict';
/**
* Vue RESTful Backend App Router module
*/
//==============================================================================
/**
* Module dependencies
*/
var express = require('express');
//------------------------------------------------------------------------------
//==============================================================================
/**
* Create router instance
*/
var router = express.Router();
//------------------------------------------------------------------------------
//==============================================================================
/**
* Module variables
*/
//------------------------------------------------------------------------------
//==============================================================================
/**
* Middleware
*/
//------------------------------------------------------------------------------
/**
* Routes
*/
//---------------------------Test route-----------------------------------------
router.get('/test', function (req, res) {
  return res.send('<marquee><h1>Hellooooo World!</h1></marquee>');
});
//---------------------------Root route-----------------------------------------
router.get('/', function (req, res) {
  return res.sendFile('index.html');//since there's no server side rendering
});
//------------------------------------------------------------------------------
//==============================================================================
/**
* Export router
*/
module.exports = router;
